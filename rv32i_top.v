`timescale 1ns / 1ps
module rv32i_top (
    input clk,
    input rst
);

// ??? PC & Instruction ???????????????????????????????
wire [31:0] pc, next_pc, pc_plus4;
wire [31:0] instruction;

// ??? Control Signals ????????????????????????????????
wire        RegWrite, MemRead, MemWrite;
wire        MemToReg, ALUSrc, Branch, Jump;
wire [1:0]  ALUOp;

// ??? Register File ??????????????????????????????????
wire [31:0] read_data_1, read_data_2, write_data;

// ??? Immediate ??????????????????????????????????????
wire [31:0] imm_out;

// ??? ALU ????????????????????????????????????????????
wire [3:0]  alu_ctrl;
wire [31:0] alu_b, alu_result;
wire        zero;

// ??? Memory ?????????????????????????????????????????
wire [31:0] mem_read_data;

// ??? Next PC Logic ??????????????????????????????????
wire [31:0] branch_target, jal_target, jalr_target;
wire        branch_taken;

assign pc_plus4      = pc + 4;
assign branch_target = pc + imm_out;
assign jal_target    = pc + imm_out;
assign jalr_target   = (read_data_1 + imm_out) & ~32'b1;
assign branch_taken  = Branch & zero;

assign next_pc = Jump         ? ((instruction[6:0] == 7'b1100111) 
                                    ? jalr_target : jal_target) :
                 branch_taken ? branch_target :
                                pc_plus4;

// ??? ALU B-side mux ?????????????????????????????????
assign alu_b = ALUSrc ? imm_out : read_data_2;

// ??? Write-back mux ?????????????????????????????????
// JAL/JALR write PC+4, load writes mem data, else ALU result
assign write_data = Jump     ? pc_plus4      :
                    MemToReg ? mem_read_data  :
                               alu_result;

// ??? Module Instantiations ??????????????????????????

rv32i_pc u_pc (
    .clk      (clk),
    .rst      (rst),
    .next_pc  (next_pc),
    .pc       (pc)
);

rv32i_imem u_imem (
    .addr        (pc),
    .instruction (instruction)
);

rv32i_control_unit u_cu (
    .opcode   (instruction[6:0]),
    .RegWrite (RegWrite),
    .MemRead  (MemRead),
    .MemWrite (MemWrite),
    .MemToReg (MemToReg),
    .ALUSrc   (ALUSrc),
    .Branch   (Branch),
    .Jump     (Jump),
    .ALUOp    (ALUOp)
);

rv32i_reg_file u_rf (
    .clk        (clk),
    .rst        (rst),
    .reg_write  (RegWrite),
    .rs1        (instruction[19:15]),
    .rs2        (instruction[24:20]),
    .rd         (instruction[11:7]),
    .write_data (write_data),
    .read_data_1(read_data_1),
    .read_data_2(read_data_2)
);

rv32i_imm_gen u_ig (
    .instruction (instruction),
    .imm_out     (imm_out)
);

rv32i_alu_control u_ac (
    .ALUOp    (ALUOp),
    .funct3   (instruction[14:12]),
    .funct7   (instruction[31:25]),
    .alu_ctrl (alu_ctrl)
);

rv32i_alu u_alu (
    .a        (read_data_1),
    .b        (alu_b),
    .alu_ctrl (alu_ctrl),
    .result   (alu_result),
    .zero     (zero)
);

rv32i_dmem u_dmem (
    .clk        (clk),
    .MemRead    (MemRead),
    .MemWrite   (MemWrite),
    .addr       (alu_result),
    .write_data (read_data_2),
    .read_data  (mem_read_data)
);

endmodule